/*
 * Potencia de 2 y 3: Se le solicita que guarde en un espacio de memoria un número y
    luego haga la potencia de ese número al cuadrado y al cubo.
 */
public class Ejer9 {
    public static void main (String [] args) {

        int numero = 2;
        
        System.out.print("El cuadrado de " + numero + " es "+ (numero * numero) +"\n");
        System.out.print("El cubo de " + numero + " es "+ (numero * numero * numero));

    }
}
